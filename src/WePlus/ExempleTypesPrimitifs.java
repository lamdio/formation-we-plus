package WePlus;

public class ExempleTypesPrimitifs {
    public static void main(String[] args) {
        byte valeurByte = 42;
        short valeurShort = 1000;
        int valeurInt = 12345;
        long valeurLong = 123456789L;  // Notez le suffixe "L" pour les littéraux long

        float valeurFloat = 3.14f;      // Notez le suffixe "f" pour les littéraux float
        double valeurDouble = 2.71828;

        char valeurChar = 'A';

        boolean valeurBool = true;

        System.out.println("byte : " + valeurByte);
        System.out.println("short : " + valeurShort);
        System.out.println("int : " + valeurInt);
        System.out.println("long : " + valeurLong);
        System.out.println("float : " + valeurFloat);
        System.out.println("double : " + valeurDouble);
        System.out.println("char : " + valeurChar);
        System.out.println("boolean : " + valeurBool);
    }
}
